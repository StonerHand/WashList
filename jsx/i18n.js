(function () {
  let lang = 'en';
  try {
    const stored = localStorage.getItem('washlist:lang');
    if (stored && I18N[stored]) lang = stored;
    else if (navigator.language && navigator.language.startsWith('ru')) lang = 'ru';
  } catch (e) {}

  const subs = new Set();

  function t(key) {
    return (I18N[lang] && I18N[lang][key]) || (I18N.en[key]) || key;
  }
  function getLang() { return lang; }
  function setLang(next) {
    if (!I18N[next] || next === lang) return;
    lang = next;
    try { localStorage.setItem('washlist:lang', lang); } catch (e) {}
    document.documentElement.lang = lang;
    subs.forEach(fn => fn(lang));
  }
  function subscribe(fn) { subs.add(fn); return () => subs.delete(fn); }
  function useLang() {
    const [v, setV] = React.useState(lang);
    React.useEffect(() => subscribe(setV), []);
    return v;
  }

  document.documentElement.lang = lang;
  window.WL = { t, getLang, setLang, useLang, LANGS: Object.keys(I18N) };
})();
